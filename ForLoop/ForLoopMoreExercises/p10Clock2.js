// Напишете програма, която да отпечатва часовете в денонощието от 0:0:0 до 23:59:59, всеки на отделен ред.
// Часовете да се изписват във формат "{час} : {минути} : {секунди} ".

function clock2() {
    
  for (h = 0; h < 24; h++) {
    for (m = 0; m < 60; m++) {
      for (s = 0; s < 60; s++) {
        console.log(`${h} : ${m} : ${s}`);
      }
    }
  }
}

clock2();
